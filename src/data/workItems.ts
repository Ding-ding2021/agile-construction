const inferTaskSourceType = (
  task: Pick<TaskItem, 'sourceType' | 'code' | 'parentPath' | 'projectName'>
): WorkItemSourceType => {
  if (task.sourceType) {
    const mapping: Record<NonNullable<TaskItem['sourceType']>, WorkItemSourceType> = {
      manual: 'project',
      template: 'project',
      wbs: 'project',
      standard: 'compliance',
    }
    return mapping[task.sourceType]
  }

  const fingerprint = `${task.code} ${task.parentPath} ${task.projectName}`
  if (/维修|保养|MNT/i.test(fingerprint)) return 'maintenance'
  if (/巡检|INSP/i.test(fingerprint)) return 'inspection'
  if (/合规|证照|审批|COM/i.test(fingerprint)) return 'compliance'
  if (/临时|ADH/i.test(fingerprint)) return 'adHoc'
  return 'project'
}

const inferDomain = (sourceType: WorkItemSourceType): WorkItemDomain => {
  switch (sourceType) {
    case 'maintenance':
      return '设备'
    case 'inspection':
      return '运营'
    case 'compliance':
      return '合规'
    case 'adHoc':
      return '通用'
    case 'project':
    default:
      return '工程'
  }
}

const parseProjectDateRange = (dateRange: string) => {
  const [rawStart, rawEnd] = dateRange.split('~').map(item => item.trim())
  return {
    start: rawStart || '2024-01-01',
    end: rawEnd || '2024-12-31',
  }
}

