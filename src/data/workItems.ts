export const getProjectWorkItems = (project: ProjectItem): WorkItem[] =>
  buildProjectWorkItemTemplates(project)

export const mapTasksToWorkItems = (tasks: TaskItem[]): WorkItem[] =>
  tasks.map((task, index) => {
    const sourceType = inferTaskSourceType(task)
    return {
      id: `task-${task.code}`,
      sourceType,
      taskCode: task.code,
      projectCode:
        sourceType === 'project' ? task.code.split('-').slice(0, 2).join('-') : undefined,
      parentId: null,
      kind: task.taskType === '里程碑' ? 'milestone' : 'task',
      wbsCode: `T.${index + 1}`,
      name: task.name,
      owner: task.owner,
      status: toWorkItemStatus(
        task.status,
        task.isBlocked || task.predecessorStatus === '前置阻塞' || task.slaStatus === '超时'
      ),
      progress: task.progress,
      planStart: task.plannedStartAt,
      planEnd: task.plannedEndAt,
      dependencies: task.predecessorStatus === '无前置任务' ? [] : [task.predecessorStatus],
      isCritical: task.riskLevel === '高风险' || task.slaStatus === '超时',
      domain: inferDomain(sourceType),
      stage: task.parentPath,
      description: `${task.projectName} / ${task.parentPath}`,
      tags: [task.taskType, task.slaStatus, task.riskLevel].filter(t => t != null) as string[],
    }
  })

export const getAllWorkItems = (project: ProjectItem, tasks: TaskItem[]): WorkItem[] => [
  ...getProjectWorkItems(project),
  ...mapTasksToWorkItems(tasks),
]
